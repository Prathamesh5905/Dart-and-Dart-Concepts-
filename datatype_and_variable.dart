void main() {
  // Variables with different data types
  int age = 25;
  double height = 5.8;
  String name = 'John';
  bool isStudent = true;

  // List
  List<int> numbers = [1, 2, 3, 4];

  // Map
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
  };

  // Dynamic variable
  dynamic dynamicVariable = 'Hello';

  // Printing values
  print('Age: $age');
  print('Height: $height');
  print('Name: $name');
  print('Is a student: $isStudent');
  print('Numbers: $numbers');
  print('Person: $person');
  print('Dynamic Variable: $dynamicVariable');
}
