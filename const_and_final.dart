void main() {
  // const variables
  const int constantValue = 42;
  const String constantString = 'Hello, const!';

  // final variables
  final int dynamicValue = 10 + 32;
  final String finalString = 'Hello, final!';

  // Printing values
  print('Constant Value: $constantValue');
  print('Constant String: $constantString');
  print('Dynamic Value: $dynamicValue');
  print('Final String: $finalString');
}
